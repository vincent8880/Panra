'use client'

import { useState, useEffect } from 'react'
import { useRouter, useSearchParams } from 'next/navigation'
import Link from 'next/link'
import { authApi, tokenStorage } from 'lib/api'

export default function LoginPage() {
  const router = useRouter()
  const searchParams = useSearchParams()
  const [identifier, setIdentifier] = useState('')
  const [password, setPassword] = useState('')
  const [loading, setLoading] = useState(false)
  const [googleLoading, setGoogleLoading] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [showEmailForm, setShowEmailForm] = useState(false)

  // Check for Google auth callback
  useEffect(() => {
    const googleAuth = searchParams?.get('google_auth')
    const token = searchParams?.get('token')
    
    if (googleAuth === 'success') {
      // Extract and store JWT token from URL
      if (token) {
        tokenStorage.set(token)
        // Remove token from URL for security
        const url = new URL(window.location.href)
        url.searchParams.delete('token')
        window.history.replaceState({}, '', url.toString())
      }
      // Redirect to home
      router.push('/')
    } else if (googleAuth === 'error') {
      const reason = searchParams?.get('reason')
      const message = searchParams?.get('message')
      if (message) {
        setError(decodeURIComponent(message))
      } else if (reason) {
        setError(`Google authentication failed: ${reason}. Please try again or use email/password login.`)
      } else {
        setError('Google authentication failed. Please try again or use email/password login.')
      }
    }
  }, [searchParams, router])

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    setError(null)
    setLoading(true)
    try {
      const response = await authApi.login({ username: identifier, email: identifier, password })
      console.log('ðŸ” [LoginPage] Login response:', response)
      const token = tokenStorage.get()
      console.log('ðŸ” [LoginPage] Token after login:', token ? `Token exists (${token.substring(0, 20)}...)` : 'âŒ NO TOKEN')
      router.push('/')
    } catch (err: any) {
      const detail = err?.response?.data?.detail || 'Login failed. Check your details and try again.'
      setError(detail)
      console.error('âŒ [LoginPage] Login error:', err)
    } finally {
      setLoading(false)
    }
  }

  const handleGoogleLogin = async () => {
    setError(null)
    setGoogleLoading(true)
    try {
      const authUrl = await authApi.getGoogleAuthUrl()
      // Redirect to Google OAuth
      window.location.href = authUrl
    } catch (err: any) {
      const detail = err?.response?.data?.detail || 'Failed to initiate Google login.'
      setError(detail)
      setGoogleLoading(false)
    }
  }

  return (
    <div className="fixed inset-0 z-50 flex items-center justify-center bg-black/60 backdrop-blur-sm p-4">
      <div className="relative w-full max-w-md bg-pm-bg-card border border-pm-border rounded-xl shadow-2xl">
        {/* Close button */}
        <button
          onClick={() => router.push('/')}
          className="absolute top-4 right-4 p-1.5 text-pm-text-secondary hover:text-pm-text-primary transition-colors"
          aria-label="Close"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
          </svg>
        </button>

        <div className="p-8">
          {/* Header */}
          <div className="mb-6 text-center">
            <h1 className="text-2xl font-semibold text-pm-text-primary mb-1">Log In</h1>
            <p className="text-sm text-pm-text-secondary">
              Welcome back to Panra
            </p>
          </div>

          {error && (
            <div className="mb-4 text-sm text-red-400 bg-red-950/20 border border-red-800/50 rounded-md px-3 py-2">
              {error}
            </div>
          )}

          {!showEmailForm ? (
            <>
              {/* Google Sign-In Button - White background like Polymarket */}
              <button
                onClick={handleGoogleLogin}
                disabled={googleLoading || loading}
                className="w-full flex items-center justify-center gap-3 py-3 px-4 rounded-lg border border-gray-300 bg-white hover:bg-gray-50 text-gray-900 font-medium text-sm transition-all disabled:opacity-50 disabled:cursor-not-allowed mb-3 shadow-sm"
              >
                {googleLoading ? (
                  <span className="flex items-center gap-2 text-gray-600">
                    <span className="inline-block w-4 h-4 border-2 border-gray-400 border-t-transparent rounded-full animate-spin"></span>
                    Connecting...
                  </span>
                ) : (
                  <>
                    <svg className="w-5 h-5" viewBox="0 0 24 24">
                      <path
                        fill="#4285F4"
                        d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                      />
                      <path
                        fill="#34A853"
                        d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                      />
                      <path
                        fill="#FBBC05"
                        d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                      />
                      <path
                        fill="#EA4335"
                        d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                      />
                    </svg>
                    Continue with Google
                  </>
                )}
              </button>

              <div className="relative my-4">
                <div className="absolute inset-0 flex items-center">
                  <div className="w-full border-t border-pm-border"></div>
                </div>
                <div className="relative flex justify-center text-xs">
                  <span className="px-3 bg-pm-bg-card text-pm-text-secondary">or</span>
                </div>
              </div>

              {/* Continue with Email button */}
              <button
                onClick={() => setShowEmailForm(true)}
                className="w-full py-3 px-4 rounded-lg border border-pm-border bg-pm-bg-secondary hover:bg-pm-bg-card text-pm-text-primary font-medium text-sm transition-all"
              >
                Continue with Email
              </button>
            </>
          ) : (
            <form onSubmit={handleSubmit} className="space-y-4">
              <div>
                <label className="block text-xs font-medium text-pm-text-secondary mb-1.5">
                  Email or username
                </label>
                <input
                  type="text"
                  value={identifier}
                  onChange={(e) => setIdentifier(e.target.value)}
                  className="pm-input w-full text-sm"
                  placeholder="Enter your email or username"
                  required
                />
              </div>

              <div>
                <label className="block text-xs font-medium text-pm-text-secondary mb-1.5">
                  Password
                </label>
                <input
                  type="password"
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  className="pm-input w-full text-sm"
                  placeholder="Enter your password"
                  required
                />
              </div>

              <button
                type="submit"
                disabled={loading || googleLoading}
                className="w-full py-3 px-4 rounded-lg bg-pm-blue hover:bg-pm-blue/90 text-white font-medium text-sm transition-colors disabled:opacity-50 disabled:cursor-not-allowed mt-4"
              >
                {loading ? 'Logging inâ€¦' : 'Log in'}
              </button>
            </form>
          )}

          {/* Footer */}
          <p className="mt-6 text-xs text-center text-pm-text-secondary">
            Don&apos;t have an account?{' '}
            <Link href="/signup" className="text-pm-blue hover:text-pm-blue/80 font-medium">
              Sign Up
            </Link>
          </p>
        </div>
      </div>
    </div>
  )
}